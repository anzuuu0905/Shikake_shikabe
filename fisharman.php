                  <b>これからも数年先の漁業を見据えて、<br>この鹿部の海を未来へ残していきたい。</b>
                  鹿部のえび漁は、近隣都市が禁漁の夏に行うのと、近海の新鮮なえびが穫れるため値がつきやすい。ただ夏なので体力的には厳しいよ。えびはすぐに鮮度が落ちてしまうので、ここでしか食べられない新鮮なえびを地元の人にこそ食べてもらいたい。
