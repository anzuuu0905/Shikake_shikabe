                <p class="txt">
                「可能性のあることは挑戦したい。」と話してくれた熊川さん。18年程前からごっこ刺し網漁を操業している。ごっこ漁は一人で行うため、大変な面もあるけど頑張った分だけ自身にも還元されるので、それが頑張れる理由だとか。そして、熊川さんにはもう一つ頑張らなくてはならない理由がある。「息子たち世代の漁業者に良い形でバトンタッチできるよう、可能性のあることには挑戦して、漁業でしっかりと利益を残せる仕組みを作っていきたいんだ。」これからも熊川さんの挑戦は続きます。
