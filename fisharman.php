                  数年前から噴火湾全域において、ほたて養殖漁業が危機的な状況を迎えている。良いと思った手法や制度はどんどん取り入れて、部会の仲間に共有する。コロナウイルスの影響もあり厳しい年になるでしょうけど、こんな時こそ地元のほたてを皆に食べて欲しい。
                </p>
              </div>
            </li>
            
            <li>
