                  能戸さん親子は、噴火湾のすけとうだら漁は不漁を極めており、養殖ほたて漁業・えび籠漁と多種多様な漁業で安定的な経営を図っている。鹿部町の稼ぎ頭であるすけとうだら漁復調の兆しを待っている。
                </p>
              </div>
            </li>

