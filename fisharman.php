                <h3 class="pcOnly">鹿部漁業協同組合 昆布採集部会 部会長<br><span>米本 繁広</span>さんと<span>部会の皆さん</span><small>(写真・右)</small></h3>
                <p class="txt">
                  <b>人が宝。今後も町を支え続けてきた昆布を、<br class="pcOnly">次の世代に残していく為、皆で一丸となって<br class="pcOnly">資源の回復に努めていきたい。</b>
                  ここ数年、海水温の上昇に伴って栄養塩類の減少・鉄分不足などから“磯焼け”現象が著しく、昆布の漁獲量が低迷しています。現在、昆布部会では立て縄による昆布種苗の増殖などに取り組んでいるほか、町や組合の支援を受け投石事業を実施する予定です。
